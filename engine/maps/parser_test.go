func TestParseData(t *testing.T) {

	var m Map

	err := xml.Unmarshal([]byte(xmlMap), &m)
	if err != nil {
		t.Error(err)
	}

	if len(m.Layers) != 2 {
		t.Errorf("invalid layers length. expected = 2, actual = %d", len(m.Layers))
	}

	layer := m.Layers[0]

	data := ParseData(&layer)
	if len(data) != 20 {
		t.Errorf("invalid data rows. Expected = %d, Actual = %d", 20, len(data))
	}

	for _, row := range data {
		fmt.Println(row)
		if len(row) != 60 {
			t.Errorf("invalid data columns. Expected = %d, Actual = %d", 60, len(row))
		}
	}
}
